    public function findTopSellingProducts(int $limit): array
    {
        return $this->createQueryBuilder('p')
            ->select('p', 'SUM(op.quantity) as total_sales')
            ->leftJoin('App\Entity\OrderProduct', 'op', 'WITH', 'op.product = p')
            ->groupBy('p.id')
            ->orderBy('total_sales', 'DESC')
            ->setMaxResults($limit)
            ->getQuery()
            ->getResult();
    }
